class CustomUser{
  final String id;
  final String username;
  final String displayName;
  final String email;
  final String photoUrl;
  final String bio;
  final DateTime timeStamp;

  CustomUser({
    required this.id,
    required this.username,
    required this.displayName,
    required this.email,
    required this.photoUrl,
    required this.bio,
    required this.timeStamp,
  });


}